using System;

namespace QUIZ09092019
{
    class BangunDatar
    {
        public int tampil_persegi(int sisi)
        {
            return sisi * sisi;
        }
        public int tampil_segitiga(int alas, int tinggi)
        {
            return alas * tinggi / 2;
        }
        public int tampil_lingkaran(int rusuk)
        {
            return 22/7 * rusuk * rusuk;
        }
    }
}